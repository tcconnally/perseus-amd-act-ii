def _resp(sid: str, payload: dict, status: int = 200):
    resp = make_response(jsonify(payload), status)
    resp.set_cookie("pv_sid", sid, max_age=IDLE_TTL_SEC, samesite="Lax",
                    secure=True, httponly=True)
    return resp


