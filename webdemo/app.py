# --- per-visitor sandbox ----------------------------------------------------
_LOCK = threading.Lock()
_SESSIONS: dict[str, dict] = {}          # sid -> {store, last, count, hits[]}
IDLE_TTL_SEC = 20 * 60
MAX_SESSIONS = 300
MAX_MEMORIES_PER_SID = 500
MAX_TEXT = 500
MAX_QUERY = 200
RATE_MAX = 60                            # requests / RATE_WINDOW / sid
RATE_WINDOW = 60.0


def _reap(now: float) -> None:
    dead = [s for s, v in _SESSIONS.items() if now - v["last"] > IDLE_TTL_SEC]
    for s in dead:
        try:
            _SESSIONS[s]["store"].close()
        except Exception:
            pass
        _SESSIONS.pop(s, None)
    while len(_SESSIONS) > MAX_SESSIONS:
        oldest = min(_SESSIONS, key=lambda s: _SESSIONS[s]["last"])
        try:
            _SESSIONS[oldest]["store"].close()
        except Exception:
            pass
        _SESSIONS.pop(oldest, None)


def _session(sid: str) -> dict:
    now = time.time()
    _reap(now)
    v = _SESSIONS.get(sid)
    if v is None:
        v = {"store": ReferenceStore(":memory:"), "last": now,
             "reqs": [], "seeded": False}
        _SESSIONS[sid] = v
    v["last"] = now
    return v


def _rate_ok(v: dict, now: float) -> bool:
    v["reqs"] = [t for t in v["reqs"] if now - t < RATE_WINDOW]
    if len(v["reqs"]) >= RATE_MAX:
        return False
    v["reqs"].append(now)
    return True


def _sid_from(req) -> str:
    return req.cookies.get("pv_sid") or uuid.uuid4().hex


def _hit_json(h) -> dict:
    return {
        "text": h.memory.text,
        "category": h.memory.category,
        "recall_count": h.memory.recall_count,
        "rank": round(h.rank, 3),
    }


# --- live inference (Fireworks AI API), budget-guarded -----------------------
# The memory layer above is CPU-only. The agent's ANSWER is generated by an
# open-weight LLM via the Fireworks AI API — the hackathon's designated inference
# partner. No serving API attests which accelerator handles a request, so we never
# label an answer with specific hardware. Because this is a public endpoint spending
# a shared credit balance, calls are hard-capped per day and in total; when the cap
# is hit (or no key is set) we fall back to a labelled memory-grounded composition
# and never fabricate an LLM answer.
FIREWORKS_API_KEY = os.environ.get("FIREWORKS_API_KEY", "")
FIREWORKS_MODEL = os.environ.get("FIREWORKS_MODEL",
                                 "accounts/fireworks/models/gpt-oss-120b")
FW_MAX_TOKENS = int(os.environ.get("FW_MAX_TOKENS", "160"))
FW_DAILY_CALLS = int(os.environ.get("FW_DAILY_CALLS", "5000"))
FW_TOTAL_CALLS = int(os.environ.get("FW_TOTAL_CALLS", "40000"))
_BUDGET = {"day": "", "calls": 0, "total": 0}
_BUDGET_LOCK = threading.Lock()


def _budget_ok() -> bool:
    today = time.strftime("%Y-%m-%d", time.gmtime())
    with _BUDGET_LOCK:
        if _BUDGET["day"] != today:
            _BUDGET["day"], _BUDGET["calls"] = today, 0
        if _BUDGET["total"] >= FW_TOTAL_CALLS or _BUDGET["calls"] >= FW_DAILY_CALLS:
            return False
        _BUDGET["calls"] += 1
        _BUDGET["total"] += 1
        return True


def infer(query: str, grounding: str):
    """Return (answer, backend_label, used_llm). Real LLM via Fireworks when keyed and
    under budget; otherwise an honest, accurately-labelled memory-grounded composition."""
    if not FIREWORKS_API_KEY:
        why = "no inference key configured on this deployment"
    elif not grounding:
        why = "nothing recalled yet to ground an answer"
    elif not _budget_ok():
        why = "the demo's daily inference budget is reached"
    else:
        import json as _json
        import urllib.request as _u
        prompt = (f"Using ONLY these remembered facts:\n{grounding}\n\n"
                  f"Answer in one or two sentences: {query}")
        body = _json.dumps({
            "model": FIREWORKS_MODEL,
            "messages": [{"role": "user", "content": prompt}],
            "max_tokens": FW_MAX_TOKENS, "temperature": 0.3,
        }).encode()
        try:
            req = _u.Request(
                "https://api.fireworks.ai/inference/v1/chat/completions",
                data=body, headers={"Authorization": f"Bearer {FIREWORKS_API_KEY}",
                                    "Content-Type": "application/json"})
            with _u.urlopen(req, timeout=30) as r:
                d = _json.loads(r.read())
            text = (d["choices"][0]["message"].get("content") or "").strip()
            if text:
                short = FIREWORKS_MODEL.split("/")[-1]
                return text, f"{short} via Fireworks AI (live)", True
        except Exception:
            pass                                   # fall through to composition
        why = "the inference call failed"
    facts = [ln[2:] for ln in grounding.splitlines() if ln.startswith("- ")]
    if facts:
        text = "Grounded in memory: " + " ".join(facts[:2])
    else:
        text = "I have no memory matching that yet - teach me a fact first."
    return text, f"memory-grounded composition (live inference paused: {why})", False


# --- API --------------------------------------------------------------------
@app.route("/api/remember", methods=["POST"])
def api_remember():
    sid = _sid_from(request)
    data = request.get_json(silent=True) or {}
    text = (data.get("text") or "").strip()[:MAX_TEXT]
    category = (data.get("category") or "fact").strip()[:60] or "fact"
    if not text:
        return _resp(sid, {"error": "empty text"}, 400)
    with _LOCK:
        v = _session(sid)
        if not _rate_ok(v, time.time()):
            return _resp(sid, {"error": "rate limited — slow down"}, 429)
        if v["store"].count(include_archived=True) >= MAX_MEMORIES_PER_SID:
            return _resp(sid, {"error": "demo store full — hit Reset"}, 409)
        v["store"].remember(category, text)
        v["store"].db.commit()
        active = v["store"].count()
    return _resp(sid, {"ok": True, "active": active})


@app.route("/api/recall", methods=["POST"])
def api_recall():
    sid = _sid_from(request)
    data = request.get_json(silent=True) or {}
    query = (data.get("query") or "").strip()[:MAX_QUERY]
    if not query:
        return _resp(sid, {"error": "empty query"}, 400)
    with _LOCK:
        v = _session(sid)
        if not _rate_ok(v, time.time()):
            return _resp(sid, {"error": "rate limited — slow down"}, 429)
        hits = [_hit_json(h) for h in v["store"].recall(query, k=5)]
        active = v["store"].count()
    # Recall (CPU) is done; generate the answer on AMD Instinct outside the lock.
    grounding = "\n".join(f"- {h['text']}" for h in hits)
    answer, backend, used_llm = infer(query, grounding)
    return _resp(sid, {"ok": True, "hits": hits, "active": active,
                       "answer": answer, "backend": backend, "used_llm": used_llm})


@app.route("/api/decay", methods=["POST"])
def api_decay():
    sid = _sid_from(request)
    with _LOCK:
        v = _session(sid)
        if not _rate_ok(v, time.time()):
            return _resp(sid, {"error": "rate limited — slow down"}, 429)
        # Simulate the passage of ~90 idle days so the decay tick has visible effect
        # in a live demo (real deployments run this on a schedule over real time).
        archived = v["store"].decay(now=time.time() + 90 * 86400)
        active = v["store"].count()
        total = v["store"].count(include_archived=True)
    return _resp(sid, {"ok": True, "archived": archived,
                       "active": active, "archived_total": total - active})


@app.route("/api/reset", methods=["POST"])
def api_reset():
    sid = _sid_from(request)
    with _LOCK:
        old = _SESSIONS.pop(sid, None)
        if old:
            try:
                old["store"].close()
            except Exception:
                pass
        _session(sid)
    return _resp(sid, {"ok": True, "active": 0})


